/*
 * Comments in C
 * Demonstrates single-line and multi-line comments
 */

#include <stdio.h>

int main() {
    // This is a single-line comment
    printf("Comments are used to explain code\n");
    
    /* This is a
       multi-line comment
       spanning multiple lines */
    printf("They are ignored by the compiler\n");
    
    return 0;
}
